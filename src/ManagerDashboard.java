    public void showData(){
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
    jComboBox1.addItem("Select Id to Delete");
        List<CashierDetail> records= dao.loadCashierData();
        for(CashierDetail emp:records){
            jComboBox1.addItem(""+emp.getCashierid());
        }
        
    jComboBox2.addItem("Select Id to Delete");
        List<OperatorDetail> oprecords= dao.loadOperatorData();
        for(OperatorDetail emp:oprecords){
            jComboBox2.addItem(""+emp.getOpid());
        }
        
    jComboBox3.addItem("Select Id to Delete");
        List<StockEntry> stockRecords= dao.loadStockData();
        for(StockEntry emp:stockRecords){
            jComboBox3.addItem(""+emp.getProductid());
        }
        
    }

